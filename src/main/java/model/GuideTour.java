package model;

/**
 *
 * @author DELL
 */
public class GuideTour {

    private int guideId;
    private String guideName;
    private int guideAge;
    private String guideImage;
    private String guideEmail;
    private String guidePhone;
    private String guideDetail;
    private String guideAddress;
    private String guideSex;
    private int guideYearExperience;

    public GuideTour() {
    }

    public GuideTour(int guideId, String guideName, int guideAge, String guideImage, String guideEmail, String guidePhone, String guideDetail, String guideAddress, String guideSex, int guideYearExperience) {
        this.guideId = guideId;
        this.guideName = guideName;
        this.guideAge = guideAge;
        this.guideImage = guideImage;
        this.guideEmail = guideEmail;
        this.guidePhone = guidePhone;
        this.guideDetail = guideDetail;
        this.guideAddress = guideAddress;
        this.guideSex = guideSex;
        this.guideYearExperience = guideYearExperience;
    }

    public int getGuideId() {
        return guideId;
    }

    public void setGuideId(int guideId) {
        this.guideId = guideId;
    }

    public String getGuideName() {
        return guideName;
    }

    public void setGuideName(String guideName) {
        this.guideName = guideName;
    }

    public int getGuideAge() {
        return guideAge;
    }

    public void setGuideAge(int guideAge) {
        this.guideAge = guideAge;
    }

    public String getGuideImage() {
        return guideImage;
    }

    public void setGuideImage(String guideImage) {
        this.guideImage = guideImage;
    }

    public String getGuideEmail() {
        return guideEmail;
    }

    public void setGuideEmail(String guideEmail) {
        this.guideEmail = guideEmail;
    }

    public String getGuidePhone() {
        return guidePhone;
    }

    public void setGuidePhone(String guidePhone) {
        this.guidePhone = guidePhone;
    }

    public String getGuideDetail() {
        return guideDetail;
    }

    public void setGuideDetail(String guideDetail) {
        this.guideDetail = guideDetail;
    }

    public String getGuideAddress() {
        return guideAddress;
    }

    public void setGuideAddress(String guideAddress) {
        this.guideAddress = guideAddress;
    }

    public String getGuideSex() {
        return guideSex;
    }

    public void setGuideSex(String guideSex) {
        this.guideSex = guideSex;
    }

    public int getGuideYearExperience() {
        return guideYearExperience;
    }

    public void setGuideYearExperience(int guideYearExperience) {
        this.guideYearExperience = guideYearExperience;
    }

   
    

}
